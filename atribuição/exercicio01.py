# Faça um programa que leia 4 números inteiros, calcule e mostre a soma desses números.

#Solicitando ao usuário os números
numero_1 = int(input('Digite o primeiro numero inteiro: '))
numero_2 = int(input('Digite o segundo numero inteiro: '))
numero_3 = int(input('Digite o terceiro numero inteiro: '))
numero_4 = int(input('Digite o quarto numero inteiro: '))

#Cálculo
soma = numero_1 + numero_2 + numero_3 + numero_4

#Exibindo os resultados
print('A soma desses numeros é', soma)