num = float(input('digite um numero: '))
if num == 5:
    print('numero igual a 5')
elif num == 200:
    print('numero igual a 200')
elif num == 400:
    print('numero igual a 400')
elif 500 <= num <= 1000:
    print('numero esta no intervalo entre 500 e 1000')
else:
    print('numero esta fora de todos os escopos')